#include "libft.h"

char	*ft_strchr(char const *str, int c)
{
	char	*s1;

	s1 = (char *)str;
	while (*s1)
	{
		if (*s1 == c)
			return (s1);
		++s1;
	}
	if (*s1 == c)
		return (s1);
	return (NULL);
}
