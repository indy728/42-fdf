#include "libft.h"

char	*ft_toupperstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ('a' <= str[i] && str[i] <= 'z')
			str[i] -= ('a' - 'A');
		++i;
	}
	return (str);
}
