#include "libft.h"

int	ft_putlstr(char const *str)
{
	int		l;
	char	*n;

	l = 0;
	n = "(null)";
	if (str)
		n = (char *)str;
	while (*n)
		l += ft_putlchar(*n++);
	return (l);
}
